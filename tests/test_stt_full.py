def score(expected: str, actual: str) -> tuple[float, str]:
    """字符匹配 + LCS 长度"""
    e, a = normalize(expected), normalize(actual)
    if not e or not a:
        return 0.0, f"empty (e={len(e)} a={len(a)})"
    # LCS
    n, m = len(e), len(a)
    dp = [[0] * (m + 1) for _ in range(n + 1)]
    for i in range(1, n + 1):
        for j in range(1, m + 1):
            if e[i - 1] == a[j - 1]:
                dp[i][j] = dp[i - 1][j - 1] + 1
            else:
                dp[i][j] = max(dp[i - 1][j], dp[i][j - 1])
    lcs = dp[n][m]
    return lcs / max(n, m), f"lcs={lcs}/{max(n,m)}={lcs/max(n,m):.0%}"


async def main():
    if not AUDIO.exists():
        print(f"❌ {AUDIO} not found")
        return 1

    with wave.open(str(AUDIO), "rb") as wf:
        n_ch = wf.getnchannels()
        sr = wf.getframerate()
        n = wf.getnframes()
        raw = wf.readframes(n)
    arr = np.frombuffer(raw, dtype=np.int16)
    if n_ch > 1:
        arr = arr.reshape(-1, n_ch).mean(axis=1).astype(np.int16)
    print(f"audio: {len(arr)/sr:.1f}s, {sr}Hz, mono")

    # 切成 100ms 帧
    samples_per_frame = sr * 100 // 1000
    frames = []
    for i in range(0, len(arr), samples_per_frame):
        chunk = arr[i:i + samples_per_frame]
        if len(chunk) < samples_per_frame:
            break
        frames.append(rtc.AudioFrame(
            data=chunk.tobytes(),
            sample_rate=sr,
            num_channels=1,
            samples_per_channel=len(chunk),
        ))
    print(f"frames: {len(frames)} @ 100ms")

    api_key = os.environ["GOOGLE_API_KEY"]
    t0 = time.time()
    print("\nsending all 28s to gemini-2.5-flash...")
    ev = await _transcribe(frames, api_key, "gemini-2.5-flash", "zh")
    elapsed = time.time() - t0
    text = ev.alternatives[0].text if ev.alternatives else ""

    print(f"\nelapsed: {elapsed:.1f}s")
    print(f"\n=== STT OUTPUT ({len(text)} chars) ===")
    print(text)
    print(f"\n=== EXPECTED ({len(EXPECTED)} chars) ===")
    print(EXPECTED)
    ratio, info = score(EXPECTED, text)
    print(f"\n=== 匹配度: {ratio:.0%} ({info}) ===")
    return 0 if ratio >= 0.7 else 1


if __name__ == "__main__":
    sys.exit(asyncio.run(main()))
